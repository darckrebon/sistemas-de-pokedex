                                O sistema de distancia sera alterado agr a distancia sera medida por blocos cada 1 bloco equivale a 100 de speed atacar te concede um bloco free ataques que exigem
                                contato so podem ser usados caso essa distancia seja percorrida ou caso use o cenario para isso (como arremssar pedrase coisas do tipo) <br>
                                *Batalhas oficiais se iniciam com 2 blocos entre os pokemons
