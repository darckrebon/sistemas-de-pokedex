                              <p>Mais um tipo especifico de ataques que é complicado de balancear em nosso rpg e para deixa-lo mais balanceado eu notei que o sistema que era usado estava ruim por conta disso eu ageitei este sistema para ser algo mais proximo do jogo</p> 

                              <ul>
                                <li>*Para o ataque aplicar multiplos hist sera considerado a quantia de hit e sua chance de continuar acertando dessa forma o jogador vai ficar jogando os dados para ver se consegue continuar acertando caso ele falhe no terceiro lançamento ele para por ali</li>
                                <li>*A contagem de dano tambem passara a ser individual o que vai tornar esses ataques mais poderosos porem mais dificeis</li>
                              </ul>
                               
                              
