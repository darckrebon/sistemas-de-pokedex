                              <h3>Sistema de stab:</h3>
                              <p>
                              Sistema de stab(Same-type attack bonus)
                              Agora pokemon que usarem de ataques de sua mesma tipagem ganharam 50% do seu dano a mais durante o calculo de dano
                              Ex:Um pokemon tipo fogo usando um Flamethrower no calculo de dano caso voce tenha 100 de dano voce dara 50 de dano mais o dano que passar caso haja um vantagem a
                              multiplicação é feita no valor total que passar essa regra tambem se aplica para habilidades defensivas <br>
                              Caso o pokemon possua Adaptability ou o treinador tenha especialista de determinado tipo os ataque vão para 100% caso hajam os dois ira para 150%</p>
                              
                              <h3>Status negativo</h3>
                               <ul>
                                 <li>Sonolento:</li>
                                  <ul><li>Reduz o dano fisico do pokemon em 50% e após 2 durnos o pokemon entra em dormindo pokemon que estiver dormindo recebera todo dano inteiro sem poder usar defesa</li></ul>
                                  <li>Paralizia:</li>
                                  <ul><li>Antes de paralizar o pokemon vai tendo sua speed reduzida</li></ul>
                               </ul>

                               
                               <h3>Ataques de prioridades</h3> 
                                <p>é muito complicado adaptar isso para nosso sistema após varias tentativas de adaptar isso eu cheguei a seguinte conclusão</p> 
                                <ul>
                                  <li>*Ao usar um ataque de prioridade o pokemon inimigo não pode contra atacar a menos que tenha a speed superior ao dobro da speed do seu pokemon</li>
                                  <li>*Usar um ataque de prioridade tambem te dara um turno extra porem não é acumulativo</li>
                                  <li>*Contra atacar com um ataque de prioridade te da um turno instantaneo ou seja mesmo sendo 3x mais lento seu pokemon tera o turno seguinte pra ele</li>
                                  <li>*Esquivar com move de prioridade NÃO te concede turno extra</li>
                                  <li>*Essas regras são aplicaveis a abilities que dão prioridade a certos moves</li>
                                </ul>

